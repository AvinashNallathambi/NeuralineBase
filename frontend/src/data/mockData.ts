// ── Helper: relative date generators ──────────────────────────────────────────
const now = new Date();

/** Return an ISO string offset by the given number of days (and optional hours/minutes). */
const daysFromNow = (days: number, hours = 0, minutes = 0): string => {
  const d = new Date(now);
  d.setDate(d.getDate() + days);
  d.setHours(now.getHours() + hours, now.getMinutes() + minutes, 0, 0);
  return d.toISOString();
};

/** Return an ISO string for today at a specific hour:minute. */
const todayAt = (hour: number, minute = 0): string => {
  const d = new Date(now);
  d.setHours(hour, minute, 0, 0);
  return d.toISOString();
};

/** Return an ISO string for a date N days ago. */
const daysAgo = (days: number): string => daysFromNow(-days);

/** Return an ISO date-only string (YYYY-MM-DD) N years ago. */
const yearsAgo = (years: number): string => {
  const d = new Date(now);
  d.setFullYear(d.getFullYear() - years);
  return d.toISOString().split('T')[0];
};

